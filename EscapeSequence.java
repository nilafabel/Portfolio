public class EscapeSequence {
    public static void main(String[] args) {
        //Escape Sequences
        // \n, \t, \', \"
        System.out.println("Anastasia Sidorova \nasidorova@gmail.com");
        System.out.println("Office hours\tMonday\t8:00-11:00");
        System.out.println("Location:\t\"Williamson\"");
        byte age = 127;
        short homenumber = 8900; 
        System.out.println("age:"+age+"\t"+"house no:"+homenumber);

    }
}
